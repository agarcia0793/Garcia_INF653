<?php
// Define two numbers
$number1 = 10;  // First number
$number2 = 5;   // Second number

// Perform arithmetic operations
$addition = $number1 + $number2;       // Adding the two numbers
$subtraction = $number1 - $number2;    // Subtracting second number from the first
$multiplication = $number1 * $number2; // Multiplying the two numbers
$division = $number1 / $number2;       // Dividing the first number by the second
$modulus = $number1 % $number2;        // Finding the remainder of the division

// Display the results
echo "Number 1: $number1\n";  
echo "Number 2: $number2\n";
echo "Addition: $addition\n";
echo "Subtraction: $subtraction\n";
echo "Multiplication: $multiplication\n";
echo "Division: $division\n";
echo "Modulus: $modulus\n";
?>